### Operadores Comparativos ###

print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)

print("Hola" > "Python")
print("Hola" < "Python")
print("Hola" >= "Zola") # Ordenación alfabética 
print("Hola" <= "Python")
print("Hola" == "Hola")
print("Hola" != "Python") 

### Operadores Lógicos ###

print(3 > 4 and "Hola" > "Python")
print(3 < 4 and "Hola" < "Python")
print(3 > 4 or "Hola" > "Python")
print(3 < 4 or "Hola" < "Python")
print(not(3 > 4))